    //criar objeto a partir da tela
    private Cartao createObject(){
        return (new Cartao(
                textId.getText().isEmpty()?0:Integer.parseInt(textId.getText()), 
                textTitular.getText(),
                textNumero.getText().isEmpty()?0:Integer.parseInt(textNumero.getText()),
                textChaveSeguranca.getText().isEmpty()?0:Integer.parseInt(textChaveSeguranca.getText()),
                (Usuario)comboUsuario.getSelectedItem()));
    }
    //preenche os componentes a partir de um objeto
