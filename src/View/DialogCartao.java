    private void buttonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarActionPerformed
        // TODO add your handling code here:
        if (textTitular.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Obrigatório");
            textTitular.requestFocus();
            return;
        }
        try{
            if (textId.getText().isEmpty()){//novo
                dao.add(createObject());
            }else{//atualizacao
                dao.update(createObject());
            }
            this.iniciaComponentes();
            this.carregaTable();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_buttonSalvarActionPerformed

    private void tableCartaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCartaoMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount()==2){
            int codigo = 
            Integer.parseInt(
                    tableCartao.getValueAt(tableCartao.getSelectedRow(), 0)+"");
            Cartao cartao = dao.get(Cartao.class, codigo);
            this.fillComponents(cartao);
        }
    }//GEN-LAST:event_tableCartaoMouseClicked

