    //criando o objeto
    private Plano createObject(){
        return (new Plano(textId.getText().isEmpty()?0:Integer.parseInt(textId.getText()), textDescricao.getText(),
                          textValor.getText().isEmpty()?0:Double.parseDouble(textValor.getText())));
    }
    //preenche a tela com valor do objeto
    private void fillComponents(Plano plano){
        textId.setText(plano.getId()+"");
        textDescricao.setText(plano.getDescricao());
        textValor.setText(plano.getValor()+"");
    }
    //listando todos registros de objetos
    private void loadTable(){
        tablePlano.setModel(
        new MyTableModel(Plano.class, dao.getAll(), tablePlano));
    }
    //listando todos registros de objetos de acordo com o filtro
    private void loadTable(String filtro){
        tablePlano.setModel(
        new MyTableModel(
                Plano.class, 
                dao.getAll(filtro), 
                tablePlano)
        );        
    }
