    private void iniciaComponentes(){
        textId.setText("");
        textNome.setText("");
        textTelefone.setText("");
        comboCidade.setSelectedIndex(0);
        textNome.requestFocus();
    }
    private Proprietario createObject(){
        return (new Proprietario(
                textId.getText().isEmpty()?0:Integer.parseInt(textId.getText()), 
                textNome.getText(), 
                textTelefone.getText(), 
                (Cidade)comboCidade.getSelectedItem()));
    }
    private void fillComponents(Proprietario proprietario){
        textId.setText(proprietario.getId()+"");
        textNome.setText(proprietario.getNome());
        textTelefone.setText(proprietario.getTelefone());
        comboCidade.setSelectedItem(proprietario.getCidade());
    }
    private void loadTable(){
        tableProprietario.setModel(
        new MyTableModel(
                Proprietario.class, 
                dao.getAll(), 
                tableProprietario)
        );
    }
    private void loadTable(String filtro){
        tableProprietario.setModel(
        new MyTableModel(
                Proprietario.class, 
                dao.getAll(filtro), 
                tableProprietario)
        );        
    }
    private void loadTable(Cidade cidade){
        tableProprietario.setModel(
        new MyTableModel(
                Proprietario.class, 
                dao.getAll(cidade), 
                tableProprietario)
        );        
    }
    private void loadComboCidade(){
        comboCidade.setModel(
        new DefaultComboBoxModel(new Vector(new CidadeDAO().getAll()))
        );
    }

