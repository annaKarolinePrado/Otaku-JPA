    private void tableProprietarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProprietarioMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount()==2){
            int codigo=
            Integer.parseInt(
                tableProprietario.getValueAt(tableProprietario.getSelectedRow(), 0)+"");
            Proprietario v = dao.get(Proprietario.class, codigo);
            this.fillComponents(v);
        }
    }//GEN-LAST:event_tableProprietarioMouseClicked

