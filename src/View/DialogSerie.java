    //criar objeto a partir da tela
    private Serie createObject() throws ParseException{
        return (new Serie(
                textId.getText().isEmpty()?0:Integer.parseInt(textId.getText()), 
                textNome.getText(), 
                textDuracao.getText(),
                format.parse(textLancamento.getText()),                
                (Produtora)comboProdutora.getSelectedItem(),
                (Categoria)comboCategoria.getSelectedItem()));
    }
    //preenche os componentes a partir de um objeto
    private void fillComponents(Serie filme){
        textId.setText(filme.getId()+"");
        textNome.setText(filme.getNome());
        textDuracao.setText(filme.getDuracao());
        textLancamento.setText(format.format(filme.getDataLancamento()));
        comboCategoria.setSelectedItem(filme.getCategoria());
        comboProdutora.setSelectedItem(filme.getProdutora());
    }
