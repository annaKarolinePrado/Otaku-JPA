    private Cidade createObject(){
        return (new Cidade(
                textId.getText().isEmpty()?0:Integer.parseInt(textId.getText()), 
                textNome.getText(), 
                (Sigla)comboSigla.getSelectedItem()));
    }
    //preenche os componentes a partir de um objeto
    private void fillComponents(Cidade cidade){
        textId.setText(cidade.getId()+"");
        textNome.setText(cidade.getNome());
        comboSigla.setSelectedItem(cidade.getSigla());
    }
