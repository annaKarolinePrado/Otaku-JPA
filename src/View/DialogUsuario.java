    private void iniciaComponentes(){
        textId.setText("");
        textNome.setText("");
        textTelefone.setText("");
        textBairro.setText("");
        textEndereco.setText("");
        textNumero.setText("");
        comboPerfil.setSelectedIndex(0);
        comboCidade.setSelectedIndex(0);
        textNome.requestFocus();
    }
    private Usuario createObject(){ 
        return (new Usuario(textId.getText().isEmpty()?0:Integer.parseInt(textId.getText()), 
                textNome.getText(),
                (Cidade)comboCidade.getSelectedItem(),
                textTelefone.getText(), 
                textEndereco.getText(),
                textId.getText().isEmpty()?0:Integer.parseInt(textNumero.getText()),
                textBairro.getText(),                
                radioF.isSelected()? Sexo.F: Sexo.M,
                (Perfil)comboPerfil.getSelectedItem()
                ));
    }
    private void fillComponents(Usuario usuario){
        textId.setText(usuario.getId()+"");
        textNome.setText(usuario.getNome());
        textTelefone.setText(usuario.getTelefone());
        textEndereco.setText(usuario.getEndereco());
        textNumero.setText(usuario.getNumero()+"");
        textBairro.setText(usuario.getBairro());
        comboPerfil.setSelectedItem(usuario.getPerfil());
        if(usuario.getSexo() == Sexo.F){
            radioF.setSelected(true);
        }else{
            radioM.setSelected(true);
        }       
        comboCidade.setSelectedItem(usuario.getCidade());
    }
    private void loadTable(){
        tableUsuario.setModel(
        new MyTableModel(
                Usuario.class, 
                dao.getAll(), 
                tableUsuario)
        );
    }
    private void loadTable(String filtro){
        tableUsuario.setModel(
        new MyTableModel(
                Usuario.class, 
                dao.getAll(filtro), 
                tableUsuario)
        );        
    }
    private void loadTable(Cidade cidade){
        tableUsuario.setModel(
        new MyTableModel(
                Usuario.class, 
                dao.getAll(cidade), 
                tableUsuario)
        );        
    }
    private void loadComboCidade(){
        comboCidade.setModel(
        new DefaultComboBoxModel(new Vector(new CidadeDAO().getAll()))
        );
    }

