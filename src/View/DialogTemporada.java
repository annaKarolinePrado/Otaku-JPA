    private void buttonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarActionPerformed
        // TODO add your handling code here:
        if (textNome.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "ObrigatÃ³rio");
            textNome.requestFocus();
            return;
        }
        try{
            if (textId.getText().isEmpty()){//novo
                dao.add(createObject());
            }else{//atualizacao
                dao.update(createObject());
            }
            this.iniciaComponentes();
            this.carregaTable();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_buttonSalvarActionPerformed

    private void tableTemporadaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTemporadaMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount()==2){
            int codigo = 
            Integer.parseInt(
                    tableTemporada.getValueAt(tableTemporada.getSelectedRow(), 0)+"");
            Temporada temporada = dao.get(Temporada.class, codigo);
            this.fillComponents(temporada);
        }
    }//GEN-LAST:event_tableTemporadaMouseClicked

